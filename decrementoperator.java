public class decrementoperator {
    public static void main(String[] args)
    {
        int x=87;
        x--; //87
        System.out.println(x--); //86
        x--;  //85
        System.out.println(x);//84
        x--; //84
        ++x;  //84
        System.out.println(--x); //83
        x--; //83
        System.out.println(x); //82
    }
}
